namespace OpenMLTD.MiriTore.Mltd.Entities {
    /// <summary>
    /// Represents a four-level ranking.
    /// </summary>
    public enum FourLevelRank {

        /// <summary>
        /// No rank (lower than rank C).
        /// </summary>
        None = 0,
        /// <summary>
        /// Rank C.
        /// </summary>
        C = 1,
        /// <summary>
        /// Rank B.
        /// </summary>
        B = 2,
        /// <summary>
        /// Rank A.
        /// </summary>
        A = 3,
        /// <summary>
        /// Rank S.
        /// </summary>
        S = 4

    }
}
